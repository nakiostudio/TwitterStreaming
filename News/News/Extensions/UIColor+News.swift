import UIKit

/**
 Helper methods
 */
extension UIColor {
    
    static func news_darkBackground() -> UIColor {
        return UIColor(red: 0.149, green: 0.184, blue: 0.278, alpha: 1.0)
    }
    
    static func news_lightBackground() -> UIColor {
        return UIColor(red: 0.882, green: 0.914, blue: 0.933, alpha: 1.0)
    }
    
    static func news_darkText() -> UIColor {
        return UIColor(red: 0.216, green: 0.235, blue: 0.298, alpha: 1.0)
    }
    
    static func news_lightText() -> UIColor {
        return UIColor(red: 0.541, green: 0.631, blue: 0.682, alpha: 1.0)
    }
    
    static func news_highlightText() -> UIColor {
        return UIColor(red: 0.345, green: 0.690, blue: 0.862, alpha: 1.0)
    }
    
}
